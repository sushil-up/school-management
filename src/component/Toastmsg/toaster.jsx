import "react-toastify/dist/ReactToastify.css";
import { toast } from "react-toastify";

export const errorMsg = (message) => {
  toast.error(message, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
   
  });
};
